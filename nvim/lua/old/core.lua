      Snacks.toggle({
        name = "Render Markdown",
        map = "<leader>um",
        which_key = true,
        notify = true,
        get = function()
          return require("render-markdown.state").enabled
        end,
        set = function(enabled)
          local m = require("render-markdown")
          if enabled then
            m.enable()
          else
            m.disable()
          end
        end,
      }):map("<leader>um")
